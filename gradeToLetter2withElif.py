def gradeToLetter2():
    grade=float(input("Enter the grade to be graded: "))
    if grade>=90:
        print("You got A")
    elif grade>=80:
        print("You got B")
    elif grade>=70:
        print("You got C")
    elif grade>=60:
        print("You got D")
    else:
        print("You got F")
gradeToLetter2()
